import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

import postcss from "postcss";
import tailwindcss from "@tailwindcss/postcss";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outFile = path.join(root, "public", "site-tailwind.css");

const sourceCss = [
  '@import "tailwindcss";',
  '@source "../public/*.html";',
  "",
].join("\n");

const result = await postcss([tailwindcss()]).process(sourceCss, {
  from: path.join(__dirname, "site-tailwind.input.css"),
  to: outFile,
  map: false,
});

const banner = [
  "/*",
  " * Generated by scripts/build-site-tailwind.mjs.",
  " * Rebuild after changing Tailwind utility classes in any public/*.html page.",
  " */",
  "",
].join("\n");

await fs.writeFile(outFile, banner + result.css, "utf8");
